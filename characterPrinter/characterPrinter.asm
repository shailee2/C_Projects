; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

; INTRODUCTION: This program prints the given input (an ASCII character) and prints it using 2 different symbols in a large font.
; I first initialized my registers, realizing that I would need counters to ensure the rows and columns would be uniform.
; I also used a counter to keep track of the address line we are at in FONT_DATA, as well as R0 which would print my string. 
; In FONT_DATA, there are 16 lines for each ASCII character, so I decided to multiply the hex value for the ASCII character by 16, and add it to the starting address.
; After this, I set up a loop to print out a character per column and once this counter was done, I would move onto the next row.
; If this counter was done, I would halt the program. In order to print out the characters, I used a BR statement.
; This decided whether the value was 0 or 1, and printed out the character that corresponded to the memory location assigned to the 0 and 1.
; This would correctly print out the character in a large font.


	.ORIG x3000
	; initializing registers
	AND R0,R0,#0 ; initializes R0 for output of string--
	AND R1,R1,#0 ; holds the actual ascii value of BIGCHAR--
	AND R2,R2,#0 ; holds the count of which character we're on
	AND R3,R3,#0 ; gives the row count--
	AND R4,R4,#0 ; gives the column count--
	AND R5,R5,#0 ; holds BIGCHAR address--
	AND R6,R6,#0 ; holds the data for each line--
	
	; setting up values
	ADD R3,R3,#8 
	ADD R3,R3,#8 ; rows are set to 16
	LD R5,BIGCHAR ; loads R5 with the BIGCHAR address

UPDCHAR	ADD R5,R5,#1 ; updates BIGCHAR address
	ADD R3,R3,#0 ; 
	BRz DONE
	LDR R1,R5,#0 ; holds the ascii value we need to print for BIGCHAR
	BRz NEWROW ; if the ascii value is null, go back to a new row.
	ADD R1,R1,R1 ; shifts R1 left by 1 bit
	ADD R1,R1,R1 ; shifts R1 left by 2 bits
	ADD R1,R1,R1 ; shifts R1 left by 3 bits
	ADD R1,R1,R1 ; shifts R1 left by 4 bits. R1 is now multiplied by 16.
	LEA R6,FONT_DATA ; loads R6 with FONT_DATA
	ADD R6,R6,R1 ; gets you to the starting address
	ADD R6,R6,R2 ; gets you to which line it's printing

	; row analysis
ROWS	ADD R3,R3,#0 ; for the BR statement only
	BRz DONE ; ends program
	LDR R6,R6,#0 ; loads starting address value into R6 (the bits checked for 0 or 1)
	AND R4,R4,#0 ; column count is reset
	ADD R4,R4,#8 ; column count is set to 8
	
	; column analysis
COLUMNS	ADD R4,R4,#0 ; for the BR statement only
	BRz UPDCHAR ; ends char and begins a new character when column count is zero.
	ADD R6,R6,#0
	BRzp ZERO ; if line is positive or zero, execute ZERO
	BRn ONE ; if line is negative, execute ONE

ZERO	LDI R0,ZE ; character assigned to zero is held in R0
	BRnzp AFTER ; loops to after

ONE	LDI R0,ON ; character assigned to one is held in R0
	BRnzp AFTER ; loops to after

AFTER	OUT ; prints R0
	ADD R6,R6,R6 ; shifts line to the next value
	ADD R4,R4,#-1 ; decrements column count
	BR COLUMNS ; 

NEWROW	LD R0,ENTER
	OUT ; prints new line character
	ADD R3,R3,#-1 ; decrements row count
	ADD R2,R2,#1 ; starting address is incremented
	LD R5,BIGCHAR ; loads R5 with BIGCHAR again
	BRnzp UPDCHAR ; once the row is over, update the character and start over in a new line
	
DONE	HALT
BIGCHAR	.FILL x5001 ; the character that you want to print big.
ENTER	.FILL x0A ; \n
ZE	.FILL x5000
ON	.FILL x5001



